 /**
 * 【广度优先遍历 (BFS - Breadth First Search)】
 *
 * 特点：
 * - 按层次逐层访问节点，先访问离根节点最近的节点
 * - 使用队列（Queue）数据结构
 * - 先访问浅层节点，再访问深层节点
 *
 * 遍历方式：
 * - 层序遍历（Level-order）：从上到下，从左到右逐层访问
 *    - 访问顺序：先访问第1层，再访问第2层，以此类推
 *    - 应用场景：打印树的层级结构、查找最短路径、树的序列化
 *
 * 时间复杂度：O(n)，n为节点数
 * 空间复杂度：O(w)，w为树的最大宽度（队列中最多节点数）
 */